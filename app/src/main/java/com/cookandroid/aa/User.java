package com.cookandroid.aa;

public class User {
    public String profile;
    public String User_blindName;
    public String User_blindPlace;

    public User() {}

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getUser_blindName() {
        return User_blindName;
    }

    public void setUser_blindName(String user_blindName) {
        User_blindName = user_blindName;
    }

    public String getUser_blindPlace() {
        return User_blindPlace;
    }

    public void setUser_blindPlace(String user_blindPlace) {
        User_blindPlace = user_blindPlace;
    }
}


