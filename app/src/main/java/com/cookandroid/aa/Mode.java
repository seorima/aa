package com.cookandroid.aa;

public class Mode {
    private String profile;
    private String name_mode;
    private int blind_height_mode;

    public Mode(){}

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getName_mode() {
        return name_mode;
    }

    public void setName_mode(String name_mode) {
        this.name_mode = name_mode;
    }

    public int getBlind_height_mode() {
        return blind_height_mode;
    }

    public void setBlind_height_mode(int blind_height_mode) {
        this.blind_height_mode = blind_height_mode;
    }
}
